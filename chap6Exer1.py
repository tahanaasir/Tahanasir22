#Exercise 1: Basic Math ☑️
#Using math module compute the following

#For a =2.3 find the ceil of a
#For a =2.3 find the floor of a
#For a = 5 find the factorial of a
#Find the value of 23
#For a=16 find the square root of a

import math

#Ceil of a
a = 2.3
ceil_result = math.ceil(a)
print(f"The ceil of {a} is: {ceil_result}")

#Floor of a
floor_result = math.floor(a)
print(f"The floor of {a} is: {floor_result}")

#Factorial of 5
a = 5
factorial_result = math.factorial(a)
print(f"The factorial of {a} is: {factorial_result}")

#Value of 23
value = 23
print(f"The value of {value} is: {value}")

#Square root of 16
a = 16
sqrt_result = math.sqrt(a)
print(f"The square root of {a} is: {sqrt_result}")
